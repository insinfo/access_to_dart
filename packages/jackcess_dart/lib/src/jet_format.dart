  static JetFormat getFormat(int versionByte) {
    switch (versionByte) {
      case 0x00:
        return version3;
      case 0x01:
        // Could be MSISAM or version 4, simplified here
        return version4;
      case 0x02:
        return version12;
      case 0x03:
        return version14;
      case 0x05:
        return version16;
      case 0x06:
        return version17;
      default:
        throw Exception("Unsupported version: $versionByte");
    }
  }


import 'dart:convert';
import 'dart:typed_data';

import 'package:jackcess_dart/jackcess_dart.dart';

import 'access_default_semantics.dart';
import 'analysis_model.dart';
import 'identifier_utils.dart';
import 'migration_auto_number_mode.dart';
import 'migration_export_mode.dart';
import 'migration_identifier_style.dart';
import 'migration_not_null_mode.dart';

class MigrationStatementBuilder {
  final MigrationIdentifierPolicy identifierPolicy;
  final MigrationAutoNumberMode autoNumberMode;
  final MigrationNotNullMode notNullMode;
  static const AccessDefaultSemanticTranslator _defaultTranslator =
      AccessDefaultSemanticTranslator();

  const MigrationStatementBuilder({
    required this.identifierPolicy,
    this.autoNumberMode = MigrationAutoNumberMode.plainInt,
    this.notNullMode = MigrationNotNullMode.relaxNotNull,
  });

  String buildSchema(
    AnalysisProject project, {
    MigrationExportMode exportMode = MigrationExportMode.schemaAndData,
  }) {
    final buffer = StringBuffer();
    buffer.writeln('-- Generated by access_to_dart');
    buffer.writeln('-- Source: ${project.source}');
    buffer.writeln(
      '-- Identifier style: ${identifierPolicy.style.cliValue}',
    );
    buffer.writeln('-- Export mode: ${exportMode.cliValue}');
    buffer.writeln('-- Auto-number mode: ${autoNumberMode.cliValue}');
    buffer.writeln('-- Not-null mode: ${notNullMode.cliValue}');
    buffer.writeln();

    for (final statement in buildPreDataStatements(project)) {
      buffer.writeln(statement);
      buffer.writeln();
    }
    for (final statement in buildPostDataStatements(project)) {
      buffer.writeln(statement);
      buffer.writeln();
    }
    return buffer.toString();
  }

  List<String> buildPreDataStatements(AnalysisProject project) {
    final statements = <String>[];
    if (_requiresPgcryptoExtension(project)) {
      statements.add('CREATE EXTENSION IF NOT EXISTS pgcrypto;');
    }
    if (_usesSequences) {
      statements.addAll(buildSequenceStatements(project));
    }
    for (final table in project.tables) {
      statements.add(buildCreateTableStatement(table));
    }
    if (_usesSequences) {
      statements.addAll(buildSequenceOwnershipStatements(project));
    }
    return statements;
  }

  List<String> buildPostDataStatements(AnalysisProject project) {
    final statements = <String>[];
    if (_usesSequences) {
      statements.addAll(buildSequenceSyncStatements(project));
    }
    statements.addAll(buildForeignKeyStatements(project));
    statements.addAll(buildIndexStatements(project));
    return statements;
  }

  List<String> buildPreInsertStatements(
    AnalysisProject project, {
    Map<String, List<Map<String, dynamic>>>? tableRowsByName,
  }) {
    if (!_usesSequences) {
      return const <String>[];
    }
    final statements = <String>[];
    for (final table in project.tables) {
      final rows = tableRowsByName?[table.name];
      if (rows == null || rows.isEmpty) {
        continue;
      }
      for (final column in table.columns) {
        if (!column.isAutoNumber) {
          continue;
        }
        final maxValue = _maxAutoNumberValue(rows, column.name);
        if (maxValue == null) {
          continue;
        }
        statements.add(
          'SELECT setval(${_regclassReference(_sequenceName(table, column))}, $maxValue, true);',
        );
      }
    }
    return statements;
  }

  String buildSeed(
    AnalysisProject project, {
    Map<String, List<Map<String, dynamic>>>? tableRowsByName,
  }) {
    final buffer = StringBuffer();
    buffer.writeln('-- Generated by access_to_dart');
    buffer.writeln('-- Source: ${project.source}');
    buffer.writeln(
      '-- Identifier style: ${identifierPolicy.style.cliValue}',
    );
    buffer.writeln('-- Auto-number mode: ${autoNumberMode.cliValue}');
    buffer.writeln('-- Not-null mode: ${notNullMode.cliValue}');
    buffer.writeln();
    for (final statement
        in buildPreInsertStatements(project, tableRowsByName: tableRowsByName)) {
      buffer.writeln(statement);
    }
    if (tableRowsByName != null && tableRowsByName.isNotEmpty) {
      buffer.writeln();
    }

    for (final table in project.tables) {
      final rows = tableRowsByName?[table.name] ?? table.sampleRows;
      if (rows.isEmpty) {
        continue;
      }
      for (final statement in buildInsertStatements(table, rows)) {
        buffer.writeln(statement);
      }
      buffer.writeln();
    }

    return buffer.toString();
  }

  String buildCreateTableStatement(AnalysisTable table) {
    final lines = <String>[];
    final primaryKeyColumns = <String>{
      ...?table.primaryKey?.columns.map((column) => column.name),
    };
    final columnsByLookup = <String, AnalysisColumn>{
      for (final column in table.columns) _columnLookupKey(column.name): column,
    };

    for (final column in table.columns) {
      final isPrimaryKey = primaryKeyColumns.contains(column.name);
      final generatedExpression =
          column.isCalculated
              ? _postgresCalculatedExpression(column, columnsByLookup)
              : null;
      if (generatedExpression != null) {
        lines.add(
          '  ${identifierPolicy.quotedColumnName(column.name)} ${_postgresType(column)} GENERATED ALWAYS AS ($generatedExpression) STORED',
        );
        continue;
      }
      final notNull =
          _shouldEmitNotNullConstraint(column, isPrimaryKey) ? ' NOT NULL' : '';
      final defaultExpression =
          column.isAutoNumber && _usesSequences
              ? _postgresSequenceDefault(table, column)
              : _postgresDefaultExpression(column);
      final defaultClause =
          defaultExpression == null ? '' : ' DEFAULT $defaultExpression';
      lines.add(
        '  ${identifierPolicy.quotedColumnName(column.name)} ${_postgresType(column)}$notNull$defaultClause',
      );
    }

    if (primaryKeyColumns.isNotEmpty) {
      final keyColumns = table.primaryKey!.columns
          .map((column) => identifierPolicy.quotedColumnName(column.name))
          .join(', ');
      lines.add('  PRIMARY KEY ($keyColumns)');
    }

    return 'CREATE TABLE ${identifierPolicy.quotedTableName(table.name)} (\n${lines.join(',\n')}\n);';
  }

  List<String> buildSequenceStatements(AnalysisProject project) {
    final statements = <String>[];
    for (final table in project.tables) {
      for (final column in table.columns) {
        if (!column.isAutoNumber) {
          continue;
        }
        statements.add(
          'CREATE SEQUENCE IF NOT EXISTS ${_quotedDbObjectName(_sequenceName(table, column))};',
        );
      }
    }
    return statements;
  }

  List<String> buildSequenceOwnershipStatements(AnalysisProject project) {
    final statements = <String>[];
    for (final table in project.tables) {
      for (final column in table.columns) {
        if (!column.isAutoNumber) {
          continue;
        }
        statements.add(
          'ALTER SEQUENCE ${_quotedDbObjectName(_sequenceName(table, column))} '
          'OWNED BY ${identifierPolicy.quotedTableName(table.name)}.${identifierPolicy.quotedColumnName(column.name)};',
        );
      }
    }
    return statements;
  }

  List<String> buildSequenceSyncStatements(AnalysisProject project) {
    final statements = <String>[];
    for (final table in project.tables) {
      for (final column in table.columns) {
        if (!column.isAutoNumber) {
          continue;
        }
        final regclass = _regclassReference(_sequenceName(table, column));
        final quotedColumn = identifierPolicy.quotedColumnName(column.name);
        statements.add(
          'SELECT setval($regclass, COALESCE(MAX($quotedColumn), 1), MAX($quotedColumn) IS NOT NULL) '
          'FROM ${identifierPolicy.quotedTableName(table.name)};',
        );
      }
    }
    return statements;
  }

  List<String> buildForeignKeyStatements(AnalysisProject project) {
    final tableNames = <String>{for (final table in project.tables) table.name};
    final statements = <String>[];
    for (final relationship in project.relationships) {
      if (!tableNames.contains(relationship.fromTable) ||
          !tableNames.contains(relationship.toTable) ||
          relationship.fromColumns.isEmpty ||
          relationship.toColumns.isEmpty ||
          relationship.fromColumns.length != relationship.toColumns.length) {
        continue;
      }
      final fromColumns = relationship.fromColumns
          .map(identifierPolicy.quotedColumnName)
          .join(', ');
      final toColumns = relationship.toColumns
          .map(identifierPolicy.quotedColumnName)
          .join(', ');
      statements.add(
        'ALTER TABLE ${identifierPolicy.quotedTableName(relationship.fromTable)} '
        'ADD CONSTRAINT ${_quotedDbObjectName(_constraintName(relationship))} '
        'FOREIGN KEY ($fromColumns) '
        'REFERENCES ${identifierPolicy.quotedTableName(relationship.toTable)} ($toColumns);',
      );
    }
    return statements;
  }

  List<String> buildIndexStatements(AnalysisProject project) {
    final statements = <String>[];
    for (final table in project.tables) {
      for (final index in table.indexes) {
        if (index.isPrimaryKey || index.columns.isEmpty) {
          continue;
        }
        final orderedColumns = index.columns
            .map(
              (column) =>
                  '${identifierPolicy.quotedColumnName(column.name)} ${column.ascending ? 'ASC' : 'DESC'}',
            )
            .join(', ');
        final unique = index.isUnique ? 'UNIQUE ' : '';
        statements.add(
          'CREATE ${unique}INDEX ${_quotedDbObjectName(_indexName(table, index))} '
          'ON ${identifierPolicy.quotedTableName(table.name)} ($orderedColumns);',
        );
      }
    }
    return statements;
  }

  Iterable<String> buildInsertStatements(
    AnalysisTable table,
    Iterable<Map<String, dynamic>> rows,
  ) sync* {
    final writableColumns = {
      for (final column in table.columns)
        if (!column.isCalculated) column.name,
    };
    final autoNumberColumns = {
      for (final column in table.columns)
        if (column.isAutoNumber) column.name: column,
    };
    final primaryKeyColumns = <String>{
      ...?table.primaryKey?.columns.map((column) => column.name),
    };
    var syntheticIntegerSeed = -1;
    var syntheticDoubleSeed = -1.0;
    for (final row in rows) {
      final effectiveRow = _normalizeRowForInsert(
        table,
        row,
        primaryKeyColumns: primaryKeyColumns,
        nextSyntheticInt: () => syntheticIntegerSeed--,
        nextSyntheticDouble: () {
          final current = syntheticDoubleSeed;
          syntheticDoubleSeed -= 1.0;
          return current;
        },
      );
      if (effectiveRow == null) {
        continue;
      }
      final columnNames = <String>[];
      final values = <String>[];

      effectiveRow.forEach((key, value) {
        if (!writableColumns.contains(key)) {
          return;
        }
        final autoNumberColumn = autoNumberColumns[key];
        if (autoNumberColumn != null &&
            _shouldOmitExplicitAutoNumber(autoNumberColumn, value)) {
          return;
        }
        columnNames.add(identifierPolicy.quotedColumnName(key));
        values.add(sqlLiteral(value));
      });

      if (columnNames.isEmpty) {
        continue;
      }

      yield 'INSERT INTO ${identifierPolicy.quotedTableName(table.name)} (${columnNames.join(', ')}) VALUES (${values.join(', ')});';
    }
  }

  String sqlLiteral(Object? value) {
    if (value == null) {
      return 'NULL';
    }
    if (value is bool) {
      return value ? 'TRUE' : 'FALSE';
    }
    if (value is num) {
      return '$value';
    }
    if (value is DateTime) {
      return "'${value.toUtc().toIso8601String()}'";
    }
    if (value is Uint8List) {
      return "'\\x${_bytesToHex(value)}'";
    }
    if (value is List<int>) {
      return "'\\x${_bytesToHex(Uint8List.fromList(value))}'";
    }

    final text = _postgresSafeText(value.toString()).replaceAll("'", "''");
    return "'$text'";
  }

  String _postgresSafeText(String value) {
    final sanitized = StringBuffer();
    for (final rune in value.runes) {
      if (rune == 0) {
        continue;
      }
      final isAllowedControl = rune == 0x09 || rune == 0x0A || rune == 0x0D;
      final isOtherControl = rune < 0x20 && !isAllowedControl;
      if (isOtherControl) {
        continue;
      }
      sanitized.writeCharCode(rune);
    }

    // Force a valid UTF-8 round-trip before handing the text to PostgreSQL.
    return utf8.decode(utf8.encode(sanitized.toString()), allowMalformed: true);
  }

  int? _maxAutoNumberValue(
    List<Map<String, dynamic>> rows,
    String columnName,
  ) {
    int? maxValue;
    for (final row in rows) {
      final value = row[columnName];
      final intValue = switch (value) {
        int() => value,
        num() => value.toInt(),
        String() => int.tryParse(value),
        _ => null,
      };
      if (intValue == null) {
        continue;
      }
      if (maxValue == null || intValue > maxValue) {
        maxValue = intValue;
      }
    }
    return maxValue;
  }

  bool _shouldOmitExplicitAutoNumber(
    AnalysisColumn column,
    Object? value,
  ) {
    if (autoNumberMode == MigrationAutoNumberMode.plainInt) {
      return false;
    }
    if (autoNumberMode == MigrationAutoNumberMode.forceSequence) {
      return true;
    }
    final numericValue = switch (value) {
      null => null,
      int() => value,
      num() => value.toInt(),
      String() => int.tryParse(value.trim()),
      _ => null,
    };
    if (numericValue == null) {
      return value == null;
    }
    return numericValue <= 0;
  }

  Map<String, dynamic>? _normalizeRowForInsert(
    AnalysisTable table,
    Map<String, dynamic> row, {
    required Set<String> primaryKeyColumns,
    required int Function() nextSyntheticInt,
    required double Function() nextSyntheticDouble,
  }) {
    if (notNullMode == MigrationNotNullMode.strict ||
        notNullMode == MigrationNotNullMode.relaxNotNull) {
      return row;
    }

    final normalized = Map<String, dynamic>.from(row);
    var hasInvalidRequired = false;
    for (final column in table.columns) {
      if (column.isCalculated) {
        continue;
      }
      final isPrimaryKey = primaryKeyColumns.contains(column.name);
      final isRequired = isPrimaryKey || column.isRequired;
      if (!isRequired) {
        continue;
      }
      final currentValue = normalized[column.name];
      final invalid = _isInvalidRequiredValue(currentValue);
      if (!invalid) {
        continue;
      }
      hasInvalidRequired = true;
      if (notNullMode == MigrationNotNullMode.skipRow) {
        return null;
      }
      if (notNullMode == MigrationNotNullMode.fix) {
        normalized[column.name] = _fixValueForRequiredColumn(
          column,
          nextSyntheticInt: nextSyntheticInt,
          nextSyntheticDouble: nextSyntheticDouble,
        );
      }
    }

    if (notNullMode == MigrationNotNullMode.skipRow && hasInvalidRequired) {
      return null;
    }
    return normalized;
  }

  bool _isInvalidRequiredValue(Object? value) {
    if (value == null) {
      return true;
    }
    if (value is String) {
      return value.isEmpty;
    }
    return false;
  }

  Object _fixValueForRequiredColumn(
    AnalysisColumn column, {
    required int Function() nextSyntheticInt,
    required double Function() nextSyntheticDouble,
  }) {
    final normalized = foldToAscii(column.typeName).toLowerCase();
    switch (normalized) {
      case 'byte':
      case 'int':
      case 'integer':
      case 'long':
      case 'complextype':
        return nextSyntheticInt();
      case 'double':
      case 'float':
      case 'numeric':
      case 'money':
      case 'currency':
      case 'smallmoney':
        return nextSyntheticDouble();
      case 'datetime':
      case 'date':
      case 'time':
        return DateTime.now().toUtc();
      case 'yesno':
      case 'boolean':
      case 'bit':
        return false;
      case 'uniqueidentifier':
      case 'guid':
        return '00000000-0000-0000-0000-000000000000';
      default:
        return '';
    }
  }

  bool _shouldEmitNotNullConstraint(
    AnalysisColumn column,
    bool isPrimaryKey,
  ) {
    if (isPrimaryKey) {
      return true;
    }
    if (!column.isRequired) {
      return false;
    }
    return notNullMode != MigrationNotNullMode.relaxNotNull;
  }

  bool get _usesSequences => autoNumberMode != MigrationAutoNumberMode.plainInt;

  String _postgresType(AnalysisColumn column) {
    final normalized = foldToAscii(column.typeName).toLowerCase();
    switch (normalized) {
      case 'byte':
      case 'int':
      case 'integer':
      case 'long':
      case 'complextype':
        return 'INTEGER';
      case 'double':
      case 'float':
        return 'DOUBLE PRECISION';
      case 'numeric':
        return _numericType(column);
      case 'money':
        return 'NUMERIC(19,2)';
      case 'currency':
      case 'smallmoney':
        return 'NUMERIC(19,4)';
      case 'datetime':
        return 'TIMESTAMP';
      case 'yesno':
      case 'boolean':
      case 'bit':
        return 'BOOLEAN';
      case 'binary':
      case 'longbinary':
        return 'BYTEA';
      case 'memo':
      case 'longtext':
      case 'ntext':
      case 'text':
      case 'char':
      case 'nchar':
      case 'nvarchar':
        return 'TEXT';
      case 'uniqueidentifier':
      case 'guid':
        return 'UUID';
      default:
        return 'TEXT';
    }
  }

  String _numericType(AnalysisColumn column) {
    final precision = column.precision;
    final scale = column.scale;
    if (precision != null && scale != null && precision > 0 && scale >= 0) {
      return 'NUMERIC($precision,$scale)';
    }
    if (precision != null && precision > 0) {
      return 'NUMERIC($precision)';
    }
    return 'NUMERIC';
  }

  String _postgresSequenceDefault(AnalysisTable table, AnalysisColumn column) {
    return 'nextval(${_regclassReference(_sequenceName(table, column))})';
  }

  String? _postgresDefaultExpression(AnalysisColumn column) {
    return _defaultTranslator.translate(column)?.postgresExpression;
  }

  String? _postgresCalculatedExpression(
    AnalysisColumn column,
    Map<String, AnalysisColumn> columnsByLookup,
  ) {
    final expression = column.calculatedExpression?.trim();
    if (expression == null || expression.isEmpty) {
      return null;
    }
    final parsed = AccessExpressionParser.tryParse(expression);
    if (parsed == null) {
      return null;
    }
    final translated = _translateAccessNode(parsed, columnsByLookup);
    if (translated == null) {
      return null;
    }
    return _coerceCalculatedExpressionForColumn(translated, column);
  }

  _TranslatedCalculatedExpression? _translateAccessNode(
    AccessExpressionNode node,
    Map<String, AnalysisColumn> columnsByLookup,
  ) {
    if (node is AccessIdentifierNode) {
      if (node.parts.isEmpty || node.parts.last == '*') {
        return null;
      }
      final resolved = _resolveColumn(node.parts.last, columnsByLookup);
      return _TranslatedCalculatedExpression(
        sql: identifierPolicy.quotedColumnName(node.parts.last),
        kind: _expressionKindForColumn(resolved),
      );
    }
    if (node is AccessStringNode) {
      return _TranslatedCalculatedExpression(
        sql: _singleQuotedLiteral(node.value),
        kind: _CalculatedExpressionKind.text,
      );
    }
    if (node is AccessNumberNode) {
      return _TranslatedCalculatedExpression(
        sql: node.value,
        kind: _CalculatedExpressionKind.number,
      );
    }
    if (node is AccessUnaryNode) {
      final operand = _translateAccessNode(node.operand, columnsByLookup);
      if (operand == null) {
        return null;
      }
      final op =
          node.operatorName.toUpperCase() == 'NOT' ? 'NOT ' : node.operatorName;
      return _TranslatedCalculatedExpression(
        sql: '($op${operand.sql})',
        kind: node.operatorName.toUpperCase() == 'NOT'
            ? _CalculatedExpressionKind.booleanValue
            : operand.kind,
      );
    }
    if (node is AccessBinaryNode) {
      final left = _translateAccessNode(node.left, columnsByLookup);
      final right = _translateAccessNode(node.right, columnsByLookup);
      if (left == null || right == null) {
        return null;
      }
      return _translateBinaryNode(node, left, right);
    }
    if (node is AccessFunctionCallNode) {
      final name = foldToAscii(node.name).toUpperCase();
      final args = node.arguments
          .map((argument) => _translateAccessNode(argument, columnsByLookup))
          .toList(growable: false);
      if (args.any((arg) => arg == null)) {
        return null;
      }
      final translatedArgs = args.cast<_TranslatedCalculatedExpression>();
      switch (name) {
        case 'NZ':
          if (translatedArgs.length == 1) {
            return _TranslatedCalculatedExpression(
              sql: 'COALESCE(${translatedArgs[0].sql}, \'\')',
              kind: translatedArgs[0].kind == _CalculatedExpressionKind.unknown
                  ? _CalculatedExpressionKind.text
                  : translatedArgs[0].kind,
            );
          }
          if (translatedArgs.length == 2) {
            return _TranslatedCalculatedExpression(
              sql:
                  'COALESCE(${translatedArgs[0].sql}, ${translatedArgs[1].sql})',
              kind: translatedArgs[0].kind != _CalculatedExpressionKind.unknown
                  ? translatedArgs[0].kind
                  : translatedArgs[1].kind,
            );
          }
          return null;
        case 'IIF':
          if (translatedArgs.length != 3) {
            return null;
          }
          return _TranslatedCalculatedExpression(
            sql:
                '(CASE WHEN ${translatedArgs[0].sql} THEN ${translatedArgs[1].sql} ELSE ${translatedArgs[2].sql} END)',
            kind: translatedArgs[1].kind != _CalculatedExpressionKind.unknown
                ? translatedArgs[1].kind
                : translatedArgs[2].kind,
          );
        case 'LEN':
          if (translatedArgs.length != 1) {
            return null;
          }
          return _TranslatedCalculatedExpression(
            sql: 'CHAR_LENGTH(${translatedArgs[0].sql})',
            kind: _CalculatedExpressionKind.number,
          );
        case 'ABS':
        case 'ROUND':
        case 'UCASE':
        case 'LCASE':
          final targetName = switch (name) {
            'UCASE' => 'UPPER',
            'LCASE' => 'LOWER',
            _ => name,
          };
          final kind = switch (name) {
            'UCASE' || 'LCASE' => _CalculatedExpressionKind.text,
            _ => translatedArgs.isEmpty
                ? _CalculatedExpressionKind.unknown
                : translatedArgs.first.kind,
          };
          return _TranslatedCalculatedExpression(
            sql:
                '$targetName(${translatedArgs.map((arg) => arg.sql).join(', ')})',
            kind: kind,
          );
        case 'ISNULL':
          if (translatedArgs.length != 1) {
            return null;
          }
          return _TranslatedCalculatedExpression(
            sql: '(${translatedArgs[0].sql} IS NULL)',
            kind: _CalculatedExpressionKind.booleanValue,
          );
        default:
          return null;
      }
    }
    return null;
  }

  _TranslatedCalculatedExpression? _translateBinaryNode(
    AccessBinaryNode node,
    _TranslatedCalculatedExpression left,
    _TranslatedCalculatedExpression right,
  ) {
    final operatorName = node.operatorName.toUpperCase();
    switch (operatorName) {
      case '&':
        return _TranslatedCalculatedExpression(
          sql: '(${left.sql} || ${right.sql})',
          kind: _CalculatedExpressionKind.text,
        );
      case '=':
      case '<>':
      case '>':
      case '<':
      case '>=':
      case '<=':
        final op = switch (operatorName) {
          '=' => '=',
          '<>' => '!=',
          '>' => '>',
          '<' => '<',
          '>=' => '>=',
          '<=' => '<=',
          _ => '',
        };
        return _TranslatedCalculatedExpression(
          sql: '(${left.sql} $op ${right.sql})',
          kind: _CalculatedExpressionKind.booleanValue,
        );
      case 'AND':
      case 'OR':
        return _TranslatedCalculatedExpression(
          sql: '(${left.sql} $operatorName ${right.sql})',
          kind: _CalculatedExpressionKind.booleanValue,
        );
      case '+':
      case '-':
        return _translateAdditiveNode(operatorName, left, right);
      case '*':
      case '/':
        return _TranslatedCalculatedExpression(
          sql: '(${left.sql} $operatorName ${right.sql})',
          kind: _inferArithmeticKind(left.kind, right.kind),
        );
      default:
        return null;
    }
  }

  _TranslatedCalculatedExpression? _translateAdditiveNode(
    String operatorName,
    _TranslatedCalculatedExpression left,
    _TranslatedCalculatedExpression right,
  ) {
    final leftIsDateLike = _isDateLikeKind(left.kind);
    final rightIsDateLike = _isDateLikeKind(right.kind);
    final leftIsNumber = left.kind == _CalculatedExpressionKind.number;
    final rightIsNumber = right.kind == _CalculatedExpressionKind.number;

    if (leftIsDateLike && rightIsNumber) {
      return _TranslatedCalculatedExpression(
        sql:
            '(${left.sql} $operatorName (${right.sql}) * INTERVAL \'1 day\')',
        kind: left.kind,
      );
    }
    if (operatorName == '+' && leftIsNumber && rightIsDateLike) {
      return _TranslatedCalculatedExpression(
        sql: '(${right.sql} + (${left.sql}) * INTERVAL \'1 day\')',
        kind: right.kind,
      );
    }
    return _TranslatedCalculatedExpression(
      sql: '(${left.sql} $operatorName ${right.sql})',
      kind: _inferArithmeticKind(left.kind, right.kind),
    );
  }

  String _coerceCalculatedExpressionForColumn(
    _TranslatedCalculatedExpression expression,
    AnalysisColumn column,
  ) {
    final targetKind = _expressionKindForColumn(column);
    return switch (targetKind) {
      _CalculatedExpressionKind.datetime => '(${expression.sql})::timestamp',
      _CalculatedExpressionKind.dateValue => '(${expression.sql})::date',
      _CalculatedExpressionKind.timeValue => '(${expression.sql})::time',
      _ => expression.sql,
    };
  }

  AnalysisColumn? _resolveColumn(
    String rawName,
    Map<String, AnalysisColumn> columnsByLookup,
  ) {
    return columnsByLookup[_columnLookupKey(rawName)];
  }

  String _columnLookupKey(String rawName) {
    return foldToAscii(rawName).toLowerCase();
  }

  _CalculatedExpressionKind _expressionKindForColumn(AnalysisColumn? column) {
    if (column == null) {
      return _CalculatedExpressionKind.unknown;
    }
    final normalized = foldToAscii(column.typeName).toLowerCase();
    switch (normalized) {
      case 'datetime':
        return _CalculatedExpressionKind.datetime;
      case 'date':
        return _CalculatedExpressionKind.dateValue;
      case 'time':
        return _CalculatedExpressionKind.timeValue;
      case 'yesno':
      case 'boolean':
      case 'bit':
        return _CalculatedExpressionKind.booleanValue;
      case 'byte':
      case 'int':
      case 'integer':
      case 'long':
      case 'double':
      case 'float':
      case 'numeric':
      case 'money':
      case 'currency':
      case 'smallmoney':
      case 'complextype':
        return _CalculatedExpressionKind.number;
      case 'memo':
      case 'longtext':
      case 'ntext':
      case 'text':
      case 'char':
      case 'nchar':
      case 'nvarchar':
        return _CalculatedExpressionKind.text;
      default:
        return _CalculatedExpressionKind.unknown;
    }
  }

  bool _isDateLikeKind(_CalculatedExpressionKind kind) {
    return kind == _CalculatedExpressionKind.datetime ||
        kind == _CalculatedExpressionKind.dateValue ||
        kind == _CalculatedExpressionKind.timeValue;
  }

  _CalculatedExpressionKind _inferArithmeticKind(
    _CalculatedExpressionKind left,
    _CalculatedExpressionKind right,
  ) {
    if (_isDateLikeKind(left)) {
      return left;
    }
    if (_isDateLikeKind(right)) {
      return right;
    }
    if (left == _CalculatedExpressionKind.number ||
        right == _CalculatedExpressionKind.number) {
      return _CalculatedExpressionKind.number;
    }
    return _CalculatedExpressionKind.unknown;
  }

  bool _requiresPgcryptoExtension(AnalysisProject project) {
    for (final table in project.tables) {
      for (final column in table.columns) {
        final semantics = _defaultTranslator.translate(column);
        if (semantics?.requiresPgcryptoExtension == true) {
          return true;
        }
      }
    }
    return false;
  }

  String _indexName(AnalysisTable table, AnalysisIndex index) {
    final raw = index.name.trim();
    if (raw.isNotEmpty) {
      return '${table.name}_$raw';
    }
    final suffix = index.columns.map((column) => column.name).join('_');
    return 'idx_${table.name}_$suffix';
  }

  String _constraintName(AnalysisRelationship relationship) {
    final raw = relationship.name.trim();
    if (raw.isNotEmpty) {
      return raw;
    }
    return 'fk_${relationship.fromTable}_${relationship.toTable}';
  }

  String _quotedDbObjectName(String rawName) {
    return identifierPolicy.quotedObjectName(rawName);
  }

  String _sequenceName(AnalysisTable table, AnalysisColumn column) {
    return '${table.name}_${column.name}_seq';
  }

  String _regclassReference(String rawName) {
    return '${_singleQuotedLiteral(identifierPolicy.quotedObjectName(rawName))}::regclass';
  }

  String _singleQuotedLiteral(String value) {
    final escaped = value.replaceAll("'", "''");
    return "'$escaped'";
  }

  String _bytesToHex(Uint8List bytes) {
    final buffer = StringBuffer();
    for (final value in bytes) {
      buffer.write(value.toRadixString(16).padLeft(2, '0'));
    }
    return buffer.toString();
  }
}

class _TranslatedCalculatedExpression {
  final String sql;
  final _CalculatedExpressionKind kind;

  const _TranslatedCalculatedExpression({
    required this.sql,
    required this.kind,
  });
}

enum _CalculatedExpressionKind {
  unknown,
  number,
  text,
  booleanValue,
  datetime,
  dateValue,
  timeValue,
}

SELECT
  DISTINCT [SISC INTERNO SCFV GRUPOS].NomeUsuario,
  [SISC INTERNO SCFV GRUPOS].DNasc AS DNASCUSUARIO,
  [SISC INTERNO SCFV GRUPOS].Idade AS IDADEUSUARIO,
  [CS RESPONSAVEL FAMILIAR SISC].Nome AS NOMERESPONS,
  [CS RESPONSAVEL FAMILIAR SISC].Parentesco AS PARENTESCORESPONS,
  [CS RESPONSAVEL FAMILIAR SISC].DNasc AS DNASCRESPON,
  [CS RESPONSAVEL FAMILIAR SISC].Celular,
  [CS RESPONSAVEL FAMILIAR SISC].NIS,
  [CS RESPONSAVEL FAMILIAR SISC].CPF,
  [CS RESPONSAVEL FAMILIAR SISC].[CERT LIVRO/FOLHA/TERMO],
  [CS RESPONSAVEL FAMILIAR SISC].[TITULO/ZONA/SEÇAO],
  [CS RESPONSAVEL FAMILIAR SISC].[Estado Civil],
  [SISC INTERNO SCFV GRUPOS].TbGrupo.Grupo,
  [SISC INTERNO SCFV GRUPOS].TbGrupo.FxEtaria,
  [SISC INTERNO SCFV GRUPOS].TbGrupo.DiaSemana,
  [SISC INTERNO SCFV GRUPOS].TbGrupo.Horario,
  [SISC INTERNO SCFV GRUPOS].TbGrupo.Tipo,
  [SISC INTERNO SCFV GRUPOS].CodFam,
  [SISC INTERNO SCFV GRUPOS].CodOfSEDet,
  [SISC INTERNO SCFV GRUPOS].NomeMae,
  [SISC INTERNO SCFV GRUPOS].Sexo AS SexoUsuario,
  [SISC INTERNO SCFV GRUPOS].[Raça/Cor] AS RacaUsuario,
  [SISC INTERNO SCFV GRUPOS].SituaçãoUsuario,
  [SISC INTERNO SCFV GRUPOS].MunicNasc AS MunicNascUsuario,
  [SISC INTERNO SCFV GRUPOS].UFNasc AS UfNascUsuario,
  [SISC INTERNO SCFV GRUPOS].NIS AS nisusuario,
  [SISC INTERNO SCFV GRUPOS].CPF AS cpfusuario,
  [SISC INTERNO SCFV GRUPOS].RG AS rgusuario,
  [SISC INTERNO SCFV GRUPOS].OrgaoRG AS oemissusuario,
  [SISC INTERNO SCFV GRUPOS].DEmisRG AS demissusuario,
  [SISC INTERNO SCFV GRUPOS].Endereço AS endUsuario,
  [SISC INTERNO SCFV GRUPOS].Bairro,
  [SISC INTERNO SCFV GRUPOS].UF,
  [SISC INTERNO SCFV GRUPOS].CEP,
  [SISC INTERNO SCFV GRUPOS].Municipio,
  [SISC INTERNO SCFV GRUPOS].Complemento,
  [SISC INTERNO SCFV GRUPOS].Referencia,
  *
FROM
  [CS RESPONSAVEL FAMILIAR SISC]
  INNER JOIN [SISC INTERNO SCFV GRUPOS] ON [CS RESPONSAVEL FAMILIAR SISC].CodFam = [SISC INTERNO SCFV GRUPOS].CodFam
WHERE
  (
    (
      (
        [SISC INTERNO SCFV GRUPOS].TbGrupo.Tipo
      )= "OFICINA SOCIOEDUCATIVA"
    )
  );

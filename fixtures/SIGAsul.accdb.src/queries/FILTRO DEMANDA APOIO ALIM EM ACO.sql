SELECT
  [FILTRO DEMANDA ATENDIMENTO].CodAtend,
  [FILTRO PARA ACO].Nome AS [EM ACO],
  [FILTRO PARA ACO].[PAF inicio],
  [FILTRO DEMANDA ATENDIMENTO].Data,
  [FILTRO DEMANDA ATENDIMENTO].TECNICO,
  [FILTRO DEMANDA ATENDIMENTO].[FUNÇÃO NO SISTEMA],
  [FILTRO DEMANDA ATENDIMENTO].USUÁRIO,
  [FILTRO DEMANDA ATENDIMENTO].CodPessoa,
  [FILTRO DEMANDA ATENDIMENTO].DemandaApoioAlim,
  [FILTRO DEMANDA ATENDIMENTO].NOME,
  [FILTRO DEMANDA ATENDIMENTO].[DIRETOR(A)],
  [FILTRO DEMANDA ATENDIMENTO].FUNÇÃO,
  [FILTRO DEMANDA ATENDIMENTO].MATRICULA,
  [FILTRO DEMANDA ATENDIMENTO].LOGO
FROM
  [FILTRO PARA ACO]
  RIGHT JOIN [FILTRO DEMANDA ATENDIMENTO] ON [FILTRO PARA ACO].CodPessoa = [FILTRO DEMANDA ATENDIMENTO].CodPessoa
ORDER BY
  [FILTRO PARA ACO].Nome;

SELECT
  csAcompPorTecnico.CodAcomp,
  Max(TbAcoRetorno.DataRetorno) AS MáxDeDataRetorno,
  csAcompPorTecnico.CodTecnico,
  csAcompPorTecnico.NomeTec,
  csAcompPorTecnico.Nome,
  csAcompPorTecnico.[Carga Horária],
  csAcompPorTecnico.[PAF inicio],
  csAcompPorTecnico.[PAF fim],
  csAcompPorTecnico.[Motivo Deslig],
  csAcompPorTecnico.Particular,
  csAcompPorTecnico.Coletivo,
  csAcompPorTecnico.Grupo,
  csAcompPorTecnico.Defic,
  csAcompPorTecnico.Idoso,
  csAcompPorTecnico.Gest,
  csAcompPorTecnico.Criança,
  csAcompPorTecnico.Adol,
  csAcompPorTecnico.[Alcool/Drogas],
  csAcompPorTecnico.ConflitoFam,

SELECT
  [LISTAGEM TOTAL BENEFICIARIOS MUNICIPAIS].Nome,
  [LISTAGEM TOTAL BENEFICIARIOS MUNICIPAIS].TipoBeneficio,
  [LISTAGEM TOTAL BENEFICIARIOS MUNICIPAIS].CodFam,
  Max([coletivo total].DataCol) AS MáxDeDataCol,
  [coletivo total].Nome,
  [coletivo total].CodFam
FROM
  [coletivo total]
  INNER JOIN [LISTAGEM TOTAL BENEFICIARIOS MUNICIPAIS] ON [coletivo total].CodPessoa = [LISTAGEM TOTAL BENEFICIARIOS MUNICIPAIS].Nome
GROUP BY
  [LISTAGEM TOTAL BENEFICIARIOS MUNICIPAIS].Nome,
  [LISTAGEM TOTAL BENEFICIARIOS MUNICIPAIS].TipoBeneficio,
  [LISTAGEM TOTAL BENEFICIARIOS MUNICIPAIS].CodFam,
  [coletivo total].Nome,
  [coletivo total].CodFam;

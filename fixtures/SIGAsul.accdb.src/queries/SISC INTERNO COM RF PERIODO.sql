  [CS RESPONSAVEL FAMILIAR SISC].Parentesco AS RESPONSPARENTESCO,
  [CS RESPONSAVEL FAMILIAR SISC].DNasc AS DNASCRESPONS,
  [CS RESPONSAVEL FAMILIAR SISC].Celular AS CELULARRESPON,
  [CS RESPONSAVEL FAMILIAR SISC].NIS AS NISRESPONS,

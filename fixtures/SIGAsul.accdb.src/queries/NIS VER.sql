SELECT
  TbComposicao.NomePessoa,
  TbComposicao.DNasPessoa,
  TbComposicao.Escola,
  TbComposicao.NIS,
  [BASE MUNICIPAL COMPLETA].[NOME-CAUNICO],
  [BASE MUNICIPAL COMPLETA].DNASCIMENTO,
  [BASE MUNICIPAL COMPLETA].[NOME-ESCOLA],
  [BASE MUNICIPAL COMPLETA].NISCADUNICO,
  [BASE MUNICIPAL COMPLETA].[NOME RUA],
  [BASE MUNICIPAL COMPLETA].NUMEROCASA,
  [BASE MUNICIPAL COMPLETA].COMPLEMEND
FROM
  TbComposicao
  LEFT JOIN [BASE MUNICIPAL COMPLETA] ON TbComposicao.NomePessoa = [BASE MUNICIPAL COMPLETA].[NOME-CAUNICO];

SELECT
  OBITOS.Nº,
  OBITOS.[DATA DA NOTIFICAÇÃO],
  OBITOS.NOME,
  TbPessoa.DataDesligPes,
  TbPessoa.MotivoDeslPes,
  OBITOS.[DATA DO ÓBITO],
  OBITOS.CRAS,
  OBITOS.SEXO,
  OBITOS.CPF,
  OBITOS.[DATA DE NASCIMENTO],
  OBITOS.IDADE,
  OBITOS.[NOME DA MÃE],
  OBITOS.ENDEREÇO,
  OBITOS.BAIRRO,
  OBITOS.TELEFONE,
  OBITOS.GESTANTE,
  OBITOS.PUERPERA
FROM
  TbPessoa
  INNER JOIN OBITOS ON TbPessoa.Nome = OBITOS.NOME
ORDER BY
  OBITOS.NOME;
